.main_code_display_picture
    LDA #0
    STA dest_screen_addr
    LDA #&58
    STA dest_screen_addr+1
    LDA #compressed_loading_screen MOD 256
    STA src_compressed_data
    LDA #compressed_loading_screen DIV 256
    STA src_compressed_data+1

; &13D0 referenced 2 times by &13E9, &13ED
.read_next_compressed_data_pair
    LDY #1                                        ; Load number of times for pixels to repeat.
    LDA (src_compressed_data),Y
    BEQ return_1                                  ; If pixel repeat count is 0 we have finished the picture so exit
    TAX                                           ; X now contains pixel repeat count.  Used in the "FOR" loop below.
    DEY
    LDA (src_compressed_data),Y                   ; Load pixel value to output.

.loop_output_next_pixels
    STA (dest_screen_addr),Y                      ; Copy pixel value to output
    INC dest_screen_addr                          ; +1 to screen address (address of next pixel to output)
    BNE skip_msb_increment
    INC dest_screen_addr+1

.skip_msb_increment
    DEX
    BNE loop_output_next_pixels
    INC src_compressed_data
    INC src_compressed_data
    BNE read_next_compressed_data_pair
    INC src_compressed_data+1
    BNE read_next_compressed_data_pair

.return_1
    RTS                                           ; Return to BASIC program (line xx)

